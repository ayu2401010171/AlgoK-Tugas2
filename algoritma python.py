# Program untuk konversi panjang dari meter ke centimeter

# Meminta pengguna memasukkan panjang dalam meter
meter = float(input("Masukkan panjang dalam meter: "))

# Mengonversi meter ke centimeter
centimeter = meter * 100

# Menampilkan hasil konversi
print(f"Panjang {meter} meter sama dengan {centimeter} centimeter.")
